/*10.Write a JavaScript program to calculate multiplication and division of two numbers (input from the user).
Sample form : sample form */
function mulDiv(a, b) {
    let multiplication = a * b;
    let division = a / b;
    console.log(`multiplication will result in ${multiplication} and division will give us ${division}`);
};

mulDiv(10, 5); // multiplication will result in 50 and division will give us 2
mulDiv(20, 50); // multiplication will result in 1000 and division will give us 0.4